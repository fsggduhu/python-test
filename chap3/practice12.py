#比较运算符
print('98大于90吗?',98>90)
print('98小于90吗?',98<90)
print('98等于90吗?',98==90)
print('98不等于90吗?',98!=90)
print('98大于等于98吗?',98>=98)
print('98小于等于98吗?',98<=98)

#逻辑运算符
print(True and True)
print(True and False)
print(False and False)
print(False and True)
print(True or True)
print(True or False)
print(False or False)
print(False or True)
print(not False)
print(not True)
#逻辑与实例
print(8>7 and 6>5)
print(8>7 and 6<5)
print(8<7 and 6<5)
print(8<7 and 6>5)
#逻辑或实例
print(8>7 or 6>5)
print(8>7 or 6<5)
print(8<7 or 6<5)
print(8<7 or 6>5)
#逻辑非实例
print(not(8>7))
print(not(8<7))